package com.controlstatement;

public class Demo {
public static void main(String[]args) {
	for (int i = 1; i<10; i++) {
		if (i==5) {
		continue;
		
	}
	System.out.println(i);

}
	int i = 10;
	System.out.println("********");
	System.out.println(i);
	System.out.println(i++);
	System.out.println(i);
System.out.println(i);
System.out.println(++i);
System.out.println(i);
}
}
